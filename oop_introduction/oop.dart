Object-oriented programming (OOP) is a programming method
that uses objects and their interactions to design and program
 applications. It is one of the most popular programming paradigms
 and is used in many programming languages,
  such as Dart, Java, C++, Python, etc.

In OOP, an object can be anything, such as a person,
a bank account, a car, or a house. Each object has
its attributes (or properties) and behavior (or methods).
For example, a person object may have the
attributes name, age and height, and the behavior walk and talk.

