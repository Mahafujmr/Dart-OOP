// dart inheritance

class father {
 late String name;
 late String address;
 late int age ;

 void fatherInfo(){
   print('My son name is ${name='Jon'}');
   print('My Home address ${address='London'}');
 }

 void sonInfo(){
   print('My Father name is ${name= 'Herry'} ');
   print('My Father Home address ${address= 'Chanada'}');
   print('My Father age ${age=45}');
 }


}